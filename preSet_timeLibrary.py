def preSet_time_library(random_number):
# Library of random times that will be chosen at random
    match random_number:
        case 1:
            return "12:0:00 PM"

        case 2:
            return "12:30:00 PM"

        case 3:
            return "01:00:00 PM"

        case 4:
            return "01:30:00 PM"

        case 5:
            return "02:00:00 PM"
        
        case 6:
            return "02:30:00 PM"
        
        case 7:
            return "03:00:00 PM"
        
        case 8:
            return "03:30:00 PM"
        
        case 9:
            return "04:00:00 PM"
        
        case 10:
            return "04:30:00 PM"

        case 11:
            return "05:00:00 PM"